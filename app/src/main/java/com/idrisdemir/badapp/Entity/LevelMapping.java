package com.idrisdemir.badapp.Entity;

public class LevelMapping{
    private int level;
    private String rank;
    private int needNextLevelExperience;

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public int getNeedNextLevelExperience() {
        return needNextLevelExperience;
    }

    public void setNeedNextLevelExperience(int needNextLevelExperience) {
        this.needNextLevelExperience = needNextLevelExperience;
    }

}

